/// The command string registered as a hook.
const HOOK_COMMAND: &str = "rwv prime";

/// Hook events we register `rwv prime` under.
const HOOK_EVENTS: &[&str] = &["SessionStart", "PreCompact"];

/// The command registered for WorktreeCreate and WorktreeRemove hooks.
/// A single binary handles both events by reading `hook_event_name` from stdin JSON.
const WORKWEAVE_HOOK_COMMAND: &str = "rwv workweave --claude-hook";

/// Workweave hook events handled by `rwv workweave --claude-hook`.
const WORKWEAVE_HOOK_EVENTS: &[&str] = &["WorktreeCreate", "WorktreeRemove"];


/// Resolve `~/.claude/settings.json`.
fn claude_settings_path() -> anyhow::Result<PathBuf> {
    let home = std::env::var("HOME").context("HOME environment variable not set")?;
    Ok(Path::new(&home).join(".claude").join("settings.json"))
}

/// Resolve `~/.claude/hooks/` directory.
fn claude_hooks_dir() -> anyhow::Result<PathBuf> {
    let home = std::env::var("HOME").context("HOME environment variable not set")?;
    Ok(Path::new(&home).join(".claude").join("hooks"))
}

/// Command substrings that identify rwv-registered hooks.
const RWV_HOOK_COMMANDS: &[&str] = &[
    "rwv prime",
    "rwv workweave --claude-hook",
    "rwv-workweave-create.sh",
    "rwv-workweave-remove.sh",
];

/// Run `rwv setup claude`.
pub fn claude() -> anyhow::Result<()> {
    claude_inner(&claude_settings_path()?)
}

/// Run `rwv setup claude --uninstall`.
pub fn claude_uninstall() -> anyhow::Result<()> {
    claude_uninstall_at(&claude_settings_path()?, &claude_hooks_dir()?)
}

/// Implementation that accepts explicit paths (for testing).
pub fn claude_uninstall_at(settings_path: &Path, hooks_dir: &Path) -> anyhow::Result<()> {
    if !settings_path.exists() {
        println!("No settings.json found — nothing to do.");
        return Ok(());
    }

    let content = std::fs::read_to_string(settings_path)
        .with_context(|| format!("failed to read {}", settings_path.display()))?;
    let mut root: Value = serde_json::from_str(&content)
        .with_context(|| format!("invalid JSON in {}", settings_path.display()))?;

    let root_obj = root
        .as_object_mut()
        .context("settings.json root is not an object")?;

    let hooks_obj = match root_obj.get_mut("hooks").and_then(|h| h.as_object_mut()) {
        Some(obj) => obj,
        None => {
            println!("No hooks registered — nothing to do.");
            return Ok(());
        }
    };

    let mut removed_count = 0usize;

    // Collect event keys first to avoid borrow issues.
    let event_keys: Vec<String> = hooks_obj.keys().cloned().collect();

    for event in &event_keys {
        if let Some(arr) = hooks_obj.get_mut(event).and_then(|v| v.as_array_mut()) {
            // For each matcher group, remove rwv hook entries.
            for group in arr.iter_mut() {
                if let Some(hook_entries) = group.get_mut("hooks").and_then(|h| h.as_array_mut()) {
                    let before = hook_entries.len();
                    hook_entries.retain(|h| {
                        let cmd = h
                            .get("command")
                            .and_then(|c| c.as_str())
                            .unwrap_or("");
                        !RWV_HOOK_COMMANDS.iter().any(|pat| cmd.contains(pat))
                    });
                    removed_count += before - hook_entries.len();
                }
            }
            // Remove groups whose hooks array is now empty.
            arr.retain(|group| {
                group
                    .get("hooks")
                    .and_then(|h| h.as_array())
                    .map(|h| !h.is_empty())
                    .unwrap_or(true) // keep groups with non-array hooks (unexpected shape)
            });
        }
    }

    // Remove event keys whose arrays are now empty.
    let empty_events: Vec<String> = hooks_obj
        .iter()
        .filter(|(_, v)| {
            v.as_array().map(|a| a.is_empty()).unwrap_or(false)
        })
        .map(|(k, _)| k.clone())
        .collect();
    for key in &empty_events {
        hooks_obj.remove(key);
    }

    // Remove legacy script files from hooks_dir.
    let legacy_scripts = ["rwv-workweave-create.sh", "rwv-workweave-remove.sh"];
    let mut removed_scripts = vec![];
    for script in &legacy_scripts {
        let path = hooks_dir.join(script);
        if path.exists() {
            std::fs::remove_file(&path)
                .with_context(|| format!("failed to remove {}", path.display()))?;
            removed_scripts.push(path);
        }
    }

    if removed_count > 0 || !removed_scripts.is_empty() {
        let output = serde_json::to_string_pretty(&root)?;
        std::fs::write(settings_path, format!("{output}\n"))
            .with_context(|| format!("failed to write {}", settings_path.display()))?;
        println!(
            "Removed {} rwv hook entr{} from {}",
            removed_count,
            if removed_count == 1 { "y" } else { "ies" },
            settings_path.display()
        );
        for script in &removed_scripts {
            println!("Removed legacy script {}", script.display());
        }
    } else {
        println!("No rwv hooks found — nothing to do.");
    }

    Ok(())
}

/// Implementation that accepts an explicit path (for testing).
pub fn claude_at(settings_path: &Path) -> anyhow::Result<()> {
    claude_inner(settings_path)
}

fn claude_inner(settings_path: &Path) -> anyhow::Result<()> {
    if !settings_path.exists() {
        bail!(
            "{} does not exist. Please run Claude Code at least once to create it.",
            settings_path.display()
        );
    }

    // Read and parse settings.json.
    let content = std::fs::read_to_string(settings_path)
        .with_context(|| format!("failed to read {}", settings_path.display()))?;
    let mut root: Value = serde_json::from_str(&content)
        .with_context(|| format!("invalid JSON in {}", settings_path.display()))?;

    let hooks = root
        .as_object_mut()
        .context("settings.json root is not an object")?
        .entry("hooks")
        .or_insert_with(|| Value::Object(serde_json::Map::new()));

    let hooks_obj = hooks
        .as_object_mut()
        .context("\"hooks\" is not an object")?;

    let mut changed = false;

    // Register `rwv prime` for SessionStart + PreCompact.
    for &event in HOOK_EVENTS {
        if ensure_command_hook_registered(hooks_obj, event, HOOK_COMMAND) {
            changed = true;
        }
    }

    // Register `rwv workweave --claude-hook` for WorktreeCreate + WorktreeRemove.
    // The same command handles both events by reading hook_event_name from stdin JSON.
    for &event in WORKWEAVE_HOOK_EVENTS {
        if ensure_command_hook_registered(hooks_obj, event, WORKWEAVE_HOOK_COMMAND) {
            changed = true;
        }
    }

    if changed {
        let output = serde_json::to_string_pretty(&root)?;
        std::fs::write(settings_path, format!("{output}\n"))
            .with_context(|| format!("failed to write {}", settings_path.display()))?;
        println!("Registered rwv hooks in {}", settings_path.display());
    } else {
        println!("rwv hooks up to date — nothing to do.");
    }

    Ok(())
}

/// Ensure `command` is present in the hook array for `event`.
/// Returns `true` if the array was modified.
fn ensure_command_hook_registered(
    hooks: &mut serde_json::Map<String, Value>,
    event: &str,
    command: &str,
) -> bool {
    let entries = hooks
        .entry(event.to_string())
        .or_insert_with(|| Value::Array(vec![]));

    let arr = match entries.as_array_mut() {
        Some(a) => a,
        None => return false, // unexpected shape — leave it alone
    };

    // Already present in any group?
    for group in arr.iter() {
        if group_contains_command(group, command) {
            return false;
        }
    }

    // Append to existing catch-all group (matcher == ""), or create one.
    let hook_entry = serde_json::json!({
        "type": "command",
        "command": command
    });

    let catchall = arr.iter_mut().find(|g| {
        g.get("matcher")
            .and_then(|m| m.as_str())
            .map(|m| m.is_empty())
            .unwrap_or(false)
    });

    match catchall {
        Some(group) => {
            if let Some(hooks_arr) = group.get_mut("hooks").and_then(|h| h.as_array_mut()) {
                hooks_arr.push(hook_entry);
            }
        }
        None => {
            arr.push(serde_json::json!({
                "matcher": "",
                "hooks": [hook_entry]
            }));
        }
    }

    true
}

/// Check whether a matcher-group already contains the given command.
fn group_contains_command(group: &Value, command: &str) -> bool {
    group
        .get("hooks")
        .and_then(|h| h.as_array())
        .map(|hooks| {
            hooks.iter().any(|h| {
                h.get("command")
                    .and_then(|c| c.as_str())
                    .map(|c| c == command)
                    .unwrap_or(false)
            })
        })
        .unwrap_or(false)
}

