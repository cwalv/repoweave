//! `rwv setup` — generate workspace-level configuration files.
//!
//! Subcommands:
//! - `agents-md`: Generate/update `AGENTS.md` at the workspace root with the
//!   same content as `rwv prime`, for non-Claude agents (Cursor, Copilot, etc.)
//!   that read AGENTS.md.
//! - `claude`: Register `rwv prime` as a Claude Code hook in
//!   `~/.claude/settings.json` (SessionStart + PreCompact). Also installs
//!   WorktreeCreate/WorktreeRemove hook scripts to `~/.claude/hooks/` and
//!   registers them. Idempotent.

use crate::prime::render_context;
use crate::workspace::WorkspaceContext;
use anyhow::{bail, Context};
use serde_json::Value;
use std::path::{Path, PathBuf};

/// Header written to generated AGENTS.md so we can detect our own files.
const GENERATED_HEADER: &str = "<!-- Generated by rwv — do not edit -->";

/// Run `rwv setup agents-md` from the given working directory.
///
/// Generates (or updates) `AGENTS.md` at the workspace root.
/// Refuses to overwrite a file that does not contain the generated-file header.
pub fn agents_md(cwd: &Path) -> anyhow::Result<()> {
    let ctx = WorkspaceContext::resolve(cwd, None)?;
    let dest = ctx.root.join("AGENTS.md");

    // If the file already exists, only overwrite if it contains our header.
    if dest.exists() {
        let existing = std::fs::read_to_string(&dest)?;
        if !existing.contains(GENERATED_HEADER) {
            anyhow::bail!(
                "AGENTS.md exists at {} but was not generated by rwv. \
                 Remove it or add the header `{}` to allow rwv to manage it.",
                dest.display(),
                GENERATED_HEADER
            );
        }
    }

    let body = render_context(&ctx);
    let content = format!("{GENERATED_HEADER}\n\n{body}");

    std::fs::write(&dest, &content)?;
    eprintln!("wrote {}", dest.display());
    Ok(())
}

